# Addition (+)
a = 10
b = 20
print(a + b)
print(10 + 30)

# Subtraction
print(b - a)

# Multiplication
print(a * b)

# Division
print(b / a)

# Modulo
print(25 % a)

# Floor division(//)
print(10.24 / 2)
print(10.24 // 2)

print(-3 / 2)
print(-3 // 2)

# Exponent operator or power operator (**)
print(10 ** 2)
